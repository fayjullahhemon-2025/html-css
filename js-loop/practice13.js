// Task 13 — Comparing Loop Types
// Write the same task — printing numbers 1 to 5 — three times: once using for, once using while, and once using do while. Compare how the code structure differs for each.
for(let i = 1;i<=5;i++){
    console.log(i);
}
let j = 1;
while(j<=5){
    console.log(j);
    j++;
}
let k = 1;
do{
    console.log(k);
    k++;
}while(k<=5);